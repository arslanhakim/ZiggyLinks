export const getProductsByCategory = (cat) => products.filter(p => p.category === cat);
export const getLowStockProducts = () => products.filter(p => p.stock <= p.lowStockThreshold && p.stock > 0);
export const getOutOfStockProducts = () => products.filter(p => p.stock === 0);
